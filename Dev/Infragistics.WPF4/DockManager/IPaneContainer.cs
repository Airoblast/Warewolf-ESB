using System;
using System.Collections.Generic;
using System.Text;
using System.Collections;

namespace Infragistics.Windows.DockManager
{
	/// <summary>
	/// Interface implemented by a class that contains 1 or more ContentPanes or other IPaneContainers
	/// </summary>
	internal interface IPaneContainer
	{
		/// <summary>
		/// Returns the collection of items within the container
		/// </summary>
		IList Panes { get; }

		/// <summary>
		/// Returns a boolean indicating whether the container can be removed.
		/// </summary>
		bool CanBeRemoved { get; }

		/// <summary>
		/// Removes the element from its subtree.
		/// </summary>
		/// <param name="pane">The item from the list to remove</param>
		bool RemovePane(object pane);
	}
}

