using System;
using System.Collections.Generic;
using System.Text;

namespace Infragistics.Math
{
	/// <summary>
	/// Constant contains several standard mathematical constants.
	/// </summary>
    public static class Constant
	{
		/// <summary>
		/// Represents Feigenbaum's constant alpha. 
		/// </summary>
		public const double Alpha = 2.50290787509589282228390287321821578;

		/// <summary>
		/// Represents Feigenbaum's constant delta. 
		/// </summary>
		public const double Delta = 4.66920160910299067185320382046620161;

		/// <summary>
		/// Represents Euler's number e. 
		/// </summary>
		public const double E = System.Math.E;

		/// <summary>
		/// Represents a vanishingly small number epsilon. 
		/// </summary>
		public const double Epsilon = 0.000000001;

		/// <summary>
		/// Represents the imaginary number i.
		/// </summary>
		public static readonly Complex I = new Complex(0, 1);

		/// <summary>
		/// Represents infinity.
		/// </summary>
		public const double Inf = Double.PositiveInfinity;

		/// <summary>
		/// Represents an undefined number. NaN means "not a number".
		/// </summary>
		public const double NaN = Double.NaN;

		/// <summary>
		/// Represents the golden ratio phi. 
		/// </summary>
		public const double Phi = 1.618033988749895;

		/// <summary>
		/// Respresents the irrational number pi.
		/// </summary>
		public const double Pi = System.Math.PI;
	}
}

